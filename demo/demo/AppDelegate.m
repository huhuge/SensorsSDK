#pragma mark - 处理推送消息

//初始化极光推送
- (void)setupJPushNotification:(NSDictionary *)launchOptions {

    JPUSHRegisterEntity * entity = [[JPUSHRegisterEntity alloc] init];
    if (@available(iOS 12.0, *)) {
        entity.types = JPAuthorizationOptionAlert|JPAuthorizationOptionBadge|JPAuthorizationOptionSound|JPAuthorizationOptionProvidesAppNotificationSettings;
    } else {
        // Fallback on earlier versions
    }
    if ([[UIDevice currentDevice].systemVersion floatValue] >= 8.0) {
        // 可以添加自定义 categories
        // NSSet<UNNotificationCategory *> *categories for iOS10 or later
        // NSSet<UIUserNotificationCategory *> *categories for iOS8 and iOS9
    }
    [JPUSHService registerForRemoteNotificationConfig:entity delegate:self];
    
    
#ifdef DEBUG
    [JPUSHService setupWithOption:launchOptions appKey:JPush_AppKey channel:@"App Store" apsForProduction:NO advertisingIdentifier:nil];
#else
    [JPUSHService setupWithOption:launchOptions appKey:JPush_AppKey channel:@"App Store" apsForProduction:YES advertisingIdentifier:nil];
#endif
}

//注册 APNs 成功并上报 DeviceToken
